function getQuestions() {
  if (typeof store !== 'undefined') {
    const qs = store.get('bible_questions');
    if (qs && qs.length) return qs;
  } else {
    const saved = localStorage.getItem('bible_questions');
    if (saved) { try { return JSON.parse(saved); } catch (_) {} }
  }
  return DEFAULT_QUESTIONS.map((q, i) => ({ ...q, id: i }));
}

function saveQuestions(qs) {
  if (typeof store !== 'undefined') {
    store.set('bible_questions', qs);
  } else {
    localStorage.setItem('bible_questions', JSON.stringify(qs));
  }
}
